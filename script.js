function displayCars(category, condition, country = 'all', year = 'all') {
    const accordion = document.getElementById("carsAccordion");
    if (!accordion) return;

    accordion.innerHTML = "";

    if (!cars[category]) {
        accordion.innerHTML = '<p class="no-results">التصنيف غير موجود</p>';
        return;
    }

    let totalCars = 0;
    let hasResults = false;

    for (let brand in cars[category]) {
        let brandCars = cars[category][brand].filter(car => {
            if (car.condition !== condition) return false;
            if (country !== 'all' && car.country !== country) return false;
            if (year !== 'all' && car.year.toString() !== year) return false;
            return true;
        });

        if (brandCars.length > 0) {
            hasResults = true;
            totalCars += brandCars.length;

            const itemDiv = document.createElement("div");
            itemDiv.className = "accordion-item";

            const header = document.createElement("button");
            header.className = "accordion-header";
            header.innerHTML = `${brand} <span class="car-count">${brandCars.length}</span>`;

            const content = document.createElement("div");
            content.className = "accordion-content";

            brandCars.forEach(car => {
                const carDiv = document.createElement("div");
                carDiv.className = "car-card";
                carDiv.onclick = () => {
                    window.location.href = `car-details.html?category=${category}&brand=${brand}&model=${car.model}`;
                };

                carDiv.innerHTML = `
                    <img src="${car.image}" alt="${brand} ${car.model}" class="car-card-image">
                    <h3>${brand} ${car.model}</h3>
                    <p class="car-year">${car.year}</p>
                    <p class="car-price">💰 ${car.price}</p>
                    <span class="badge ${car.condition}">${car.condition === 'new' ? 'جديد' : 'مستعمل'}</span>
                `;

                content.appendChild(carDiv);
            });

            header.addEventListener("click", function() {
                this.classList.toggle("active");
                content.classList.toggle("open");
            });

            itemDiv.appendChild(header);
            itemDiv.appendChild(content);
            accordion.appendChild(itemDiv);
        }
    }

    const resultsCount = document.getElementById('resultsCount');
    if (resultsCount) {
        resultsCount.textContent = `${totalCars} سيارة`;
    }

    if (!hasResults) {
        accordion.innerHTML = '<p class="no-results">لا توجد سيارات متاحة</p>';
    } else {
        const firstHeader = accordion.querySelector('.accordion-header');
        const firstContent = accordion.querySelector('.accordion-content');
        if (firstHeader && firstContent) {
            firstHeader.classList.add('active');
            firstContent.classList.add('open');
        }
    }
}

