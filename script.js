// دالة لحساب عدد الماركات لكل دولة
function countBrandsByCountry() {
    const countries = {
        "ألمانيا": new Set(),
        "اليابان": new Set(),
        "كوريا الجنوبية": new Set(),
        "فرنسا": new Set()
    };

    for (let category in cars) {
        for (let brand in cars[category]) {
            for (let car of cars[category][brand]) {
                if (countries.hasOwnProperty(car.country)) {
                    countries[car.country].add(brand);
                }
            }
        }
    }

    return {
        "ألمانيا": countries["ألمانيا"].size,
        "اليابان": countries["اليابان"].size,
        "كوريا الجنوبية": countries["كوريا الجنوبية"].size,
        "فرنسا": countries["فرنسا"].size
    };
}

